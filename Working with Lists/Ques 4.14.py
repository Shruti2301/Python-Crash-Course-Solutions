
# 4-14. PEP 8
#
# I read through the Python Enhancement Proposal (PEP 8) Style Guide.
#
# Some important guidelines include:
# - Use 4 spaces for each level of indentation.
# - Limit lines to 79 characters when possible.
# - Use meaningful variable and function names.
# - Leave two blank lines between top-level functions and classes.
# - Add spaces around operators (e.g., x = 5 instead of x=5).
# - Use blank lines to separate sections of code for readability.
# - Follow consistent naming conventions (snake_case for variables and functions).
#
# PEP 8 helps make Python code more readable and consistent.