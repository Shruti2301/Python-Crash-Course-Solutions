# 6-2. Favorite Numbers: Use a dictionary to store people's favorite numbers.

# Think of five people you know, and use their names as keys in a dictionary.
# Store each person's favorite number as the value.
# Print each person's name and their favorite number.


favorite_numbers = {
    "Shruti": 7,
    "Smitha": 10,
    "John": 5,
    "Ray": 21,
    "Aurora": 3
}

print(f"Shruti's favorite number is {favorite_numbers['Shruti']}.")
print(f"Smitha's favorite number is {favorite_numbers['Smitha']}.")
print(f"John's favorite number is {favorite_numbers['John']}.")
print(f"Ray's favorite number is {favorite_numbers['Ray']}.")
print(f"Aurora's favorite number is {favorite_numbers['Aurora']}.")